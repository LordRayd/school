	CLO
	MOV BL, C0
LIRE:
	IN 00
	CMP AL, 0D
	JZ FINLIRE
	MOV [BL], AL
	INC BL
	JMP LIRE
FINLIRE:
	DEC BL
	MOV AL, C0
PALIN:
	CMP AL, BL
	JNS OUI
	MOV CL, [AL]
	MOV DL, [BL]
	CMP CL, DL
	JNZ NON
	INC AL
	DEC BL
	JMP PALIN
OUI:
	MOV AL, [A0]
	MOV [D0], AL
	MOV AL, [A1]
	MOV [D1], AL
	MOV AL, [A2]
	MOV [D2], AL
	JMP FINPALIN
NON:
	MOV AL, [A4]
	MOV [D0], AL
	MOV AL, [A5]
	MOV [D1], AL
	MOV AL, [A6]
	MOV [D2], AL
FINPALIN:
	ORG A0
	DB "OUI"
	DB 0
	DB "NON"
	DB 0
	END